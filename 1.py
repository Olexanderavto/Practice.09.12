# 1. Обчислення площі еліпса

# Функція повинна обчислювати площу еліпса за великими і малими напівосями.

import math


def ellipse_area(a, b):

    return math.pi * a * b

a = float(input("\nВведіть довжину великої напівосі:"))
b = float(input("Введіть довжину малої напівосі:"))

area = ellipse_area(a, b)
print(f"\nПлоща еліпса з напівосями {a} і {b} = {area:.2f}")

